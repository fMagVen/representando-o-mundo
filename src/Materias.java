public class Materias {
    private String nome;
    private int periodo;

    public void setNome(String nome){
        this.nome = nome;
    }

    public String getNome(){
        return this.nome;
    }

    public void setPeriodo(int periodo){
        this.periodo = periodo;
    }

    public int getPeriodo(){
        return this.periodo;
    }
}
